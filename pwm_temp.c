char* convert(int Temp)
{	 
	shi = Temp/1000;	  
	ge = Temp/100%10;
	shifen = Temp/10%10;
	baifen = Temp%10;
	str[0] = shi+48;
	str[1] = ge+48;
	str[2] = '.';
	str[3] = shifen+48;
	str[4] = baifen+48;
	return str;	
}
